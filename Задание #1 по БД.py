import sqlite3

# –ü–æ–¥–∫–ª—é—á–µ–Ω–∏–µ –∫ –±–∞–∑–µ –¥–∞–Ω–Ω—ã—Ö SQLite
# –°–æ–∑–¥–∞–µ–º –∏–ª–∏ –æ—Ç–∫—Ä—ã–≤–∞–µ–º —Ñ–∞–π–ª '–ó–∞–¥–∞–Ω–∏–µ –ø–æ –ë–î.db'
db = sqlite3.connect('–ó–∞–¥–∞–Ω–∏–µ –ø–æ –ë–î.db')

# –°–æ–∑–¥–∞–µ–º –∫—É—Ä—Å–æ—Ä –¥–ª—è –≤—ã–ø–æ–ª–Ω–µ–Ω–∏—è SQL-–∫–æ–º–∞–Ω–¥
c = db.cursor()

# –ï—Å–ª–∏ –Ω—É–∂–Ω–æ —É–¥–∞–ª–∏—Ç—å —Ç–∞–±–ª–∏—Ü—É –∏ –Ω–∞—á–∞—Ç—å –∑–∞–Ω–æ–≤–æ, —Ä–∞—Å–∫–æ–º–º–µ–Ω—Ç–∏—Ä–æ–≤–∞—Ç—å —ç—Ç—É —Å—Ç—Ä–æ–∫—É
# c.execute("DROP TABLE IF EXISTS tasks")

# –°–æ–∑–¥–∞–µ–º —Ç–∞–±–ª–∏—Ü—É 'tasks', –µ—Å–ª–∏ –æ–Ω–∞ –µ—â–µ –Ω–µ —Å—É—â–µ—Å—Ç–≤—É–µ—Ç
# –°—Ç—Ä—É–∫—Ç—É—Ä–∞ —Ç–∞–±–ª–∏—Ü—ã:
# id - —É–Ω–∏–∫–∞–ª—å–Ω—ã–π –∏–¥–µ–Ω—Ç–∏—Ñ–∏–∫–∞—Ç–æ—Ä (–∞–≤—Ç–æ—É–≤–µ–ª–∏—á–µ–Ω–∏–µ)
# task_name - –Ω–∞–∑–≤–∞–Ω–∏–µ –∑–∞–¥–∞—á–∏ (—Ç–µ–∫—Å—Ç)
# priority - –ø—Ä–∏–æ—Ä–∏—Ç–µ—Ç –∑–∞–¥–∞—á–∏ (—Ç–µ–∫—Å—Ç)
# status - —Å—Ç–∞—Ç—É—Å –≤—ã–ø–æ–ª–Ω–µ–Ω–∏—è (—Ç–µ–∫—Å—Ç)
# created_data - –¥–∞—Ç–∞ —Å–æ–∑–¥–∞–Ω–∏—è (—Ç–µ–∫—Å—Ç)
c.execute("""CREATE TABLE IF NOT EXISTS tasks (
id INTEGER PRIMARY KEY AUTOINCREMENT,
task_name TEXT,
priority TEXT,
status TEXT,
created_data TEXT
          )""")

# –î–æ–±–∞–≤–ª—è–µ–º —Ç–µ—Å—Ç–æ–≤—ã–µ –¥–∞–Ω–Ω—ã–µ –≤ —Ç–∞–±–ª–∏—Ü—É
# INSERT OR IGNORE –¥–æ–±–∞–≤–ª—è–µ—Ç –∑–∞–ø–∏—Å—å —Ç–æ–ª—å–∫–æ –µ—Å–ª–∏ –µ–µ –µ—â–µ –Ω–µ—Ç (–ø–æ PRIMARY KEY)
c.execute("INSERT OR IGNORE INTO tasks VALUES (1,'–ú–∞—Ç–µ–º–∞—Ç–∏–∫–∞' , '–í—ã—Å–æ–∫–∏–π', '–≤ –ø—Ä–æ—Ü–µ—Å—Å–µ', '2026-01-01')")
c.execute("INSERT OR IGNORE INTO tasks VALUES (2,'–õ–∏—Ç–µ—Ä–∞—Ç—É—Ä–∞' , '–ù–∏–∑–∫–∏–π', '–≤ –ø—Ä–æ—Ü–µ—Å—Å–µ', '2026-01-01')")
c.execute("INSERT OR IGNORE INTO tasks VALUES (3,'–§–∏–∑–∏–∫–∞' , '–°—Ä–µ–¥–Ω–∏–π', '–≤—ã–ø–æ–ª–Ω–µ–Ω–æ', '2026-01-01')")
c.execute("INSERT OR IGNORE INTO tasks VALUES (4,'–§–∏–∑–∫—É–ª—å—Ç—É—Ä–∞' , '–Ω–∏–∑–∫–∏–π', '–≤—ã–ø–æ–ª–Ω–µ–Ω–æ', '2026-01-01')")


# –§—É–Ω–∫—Ü–∏—è –¥–ª—è –æ—Ç–æ–±—Ä–∞–∂–µ–Ω–∏—è –≤—Å–µ—Ö –∑–∞–¥–∞—á –∏–∑ —Ç–∞–±–ª–∏—Ü—ã
def show_all_tasks():
    # –í—ã–ø–æ–ª–Ω—è–µ–º SQL-–∑–∞–ø—Ä–æ—Å –¥–ª—è –ø–æ–ª—É—á–µ–Ω–∏—è –≤—Å–µ—Ö –∑–∞–ø–∏—Å–µ–π –∏–∑ —Ç–∞–±–ª–∏—Ü—ã tasks
    c.execute("SELECT * FROM tasks")

    # –ü–æ–ª—É—á–∞–µ–º –≤—Å–µ —Ä–µ–∑—É–ª—å—Ç–∞—Ç—ã –∑–∞–ø—Ä–æ—Å–∞ –≤ –≤–∏–¥–µ —Å–ø–∏—Å–∫–∞ –∫–æ—Ä—Ç–µ–∂–µ–π
    tasks = c.fetchall()  # c.fetchall() –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç —Å–ø–∏—Å–æ–∫ –∫–æ—Ä—Ç–µ–∂–µ–π

    # –ï—Å–ª–∏ —Å–ø–∏—Å–æ–∫ –∑–∞–¥–∞—á –ø—É—Å—Ç, –≤—ã–≤–æ–¥–∏–º —Å–æ–æ–±—â–µ–Ω–∏–µ –∏ –≤—ã—Ö–æ–¥–∏–º –∏–∑ —Ñ—É–Ω–∫—Ü–∏–∏
    if not tasks:
        print("–°–ø–∏—Å–æ–∫ –∑–∞–¥–∞—á –ø—É—Å—Ç!")
        return

    # –í—ã–≤–æ–¥–∏–º –∑–∞–≥–æ–ª–æ–≤–æ–∫
    print("\n=== –í–°–ï –ó–ê–î–ê–ß–ò ===")

    # –ü—Ä–æ—Ö–æ–¥–∏–º –ø–æ –≤—Å–µ–º –∑–∞–¥–∞—á–∞–º –∏ –≤—ã–≤–æ–¥–∏–º –∏—Ö –≤ —É–¥–æ–±–Ω–æ–º —Ñ–æ—Ä–º–∞—Ç–µ
    # task[0] - id, task[1] - –Ω–∞–∑–≤–∞–Ω–∏–µ, task[2] - –ø—Ä–∏–æ—Ä–∏—Ç–µ—Ç, task[3] - —Å—Ç–∞—Ç—É—Å, task[4] - –¥–∞—Ç–∞
    for task in tasks:
        print(f"{task[0]}. {task[1]} [{task[2].upper()}] - {task[3]} (—Å–æ–∑–¥–∞–Ω–æ: {task[4]})")


# –í—Å–ø–æ–º–æ–≥–∞—Ç–µ–ª—å–Ω–∞—è —Ñ—É–Ω–∫—Ü–∏—è –¥–ª—è –ø—Ä–æ–≤–µ—Ä–∫–∏ –≤–≤–æ–¥–∞ 'stop' (–æ—Ç–º–µ–Ω–∞ –æ–ø–µ—Ä–∞—Ü–∏–∏)
def proverka_stop(further_stop):
    # –ó–∞–ø—Ä–∞—à–∏–≤–∞–µ–º –≤–≤–æ–¥ —Å –ø–µ—Ä–µ–¥–∞–Ω–Ω—ã–º –ø—Ä–∏–≥–ª–∞—à–µ–Ω–∏–µ–º
    request = input(further_stop)

    # –ï—Å–ª–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –≤–≤–µ–ª 'stop' (–≤ –ª—é–±–æ–º —Ä–µ–≥–∏—Å—Ç—Ä–µ), –≤–æ–∑–≤—Ä–∞—â–∞–µ–º None
    if request.lower() == 'stop':
        return None

    # –ò–Ω–∞—á–µ –≤–æ–∑–≤—Ä–∞—â–∞–µ–º –≤–≤–µ–¥–µ–Ω–Ω–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ
    return request


# –í—Å–ø–æ–º–æ–≥–∞—Ç–µ–ª—å–Ω–∞—è —Ñ—É–Ω–∫—Ü–∏—è –¥–ª—è –ø—Ä–æ–≤–µ—Ä–∫–∏ –ø—Ä–∏ —É–¥–∞–ª–µ–Ω–∏–∏ –∑–∞–¥–∞—á
def proverka_stop_delete(stop_delete):
    # –ó–∞–ø—Ä–∞—à–∏–≤–∞–µ–º –≤–≤–æ–¥ —Å –ø–µ—Ä–µ–¥–∞–Ω–Ω—ã–º –ø—Ä–∏–≥–ª–∞—à–µ–Ω–∏–µ–º
    request = input(stop_delete)

    # –ï—Å–ª–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –≤–≤–µ–ª '1', –≤–æ–∑–≤—Ä–∞—â–∞–µ–º None (–æ—Ç–º–µ–Ω–∞)
    if request.lower() == '1':
        return None

    # –ò–Ω–∞—á–µ –≤–æ–∑–≤—Ä–∞—â–∞–µ–º –≤–≤–µ–¥–µ–Ω–Ω–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ
    return request


# –§—É–Ω–∫—Ü–∏—è –¥–ª—è –¥–æ–±–∞–≤–ª–µ–Ω–∏—è –Ω–æ–≤–æ–π –∑–∞–¥–∞—á–∏
def add_task():
    print("\n=== –î–û–ë–ê–í–õ–ï–ù–ò–ï –ù–û–í–û–ô –ó–ê–î–ê–ß–ò ===")
    print("–≤–≤–µ–¥–∏—Ç–µ 'stop' –≤ –ª—é–±–æ–µ –≤—Ä–µ–º—è –¥–ª—è –æ—Ç–º–µ–Ω—ã")

    # –ó–∞–ø—Ä–∞—à–∏–≤–∞–µ–º –Ω–∞–∑–≤–∞–Ω–∏–µ –∑–∞–¥–∞—á–∏ —Å –≤–æ–∑–º–æ–∂–Ω–æ—Å—Ç—å—é –æ—Ç–º–µ–Ω—ã
    task_name = proverka_stop("–í–≤–µ–¥–∏—Ç–µ –Ω–∞–∑–≤–∞–Ω–∏–µ –∑–∞–¥–∞—á–∏: ")
    if task_name is None:  # –ï—Å–ª–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –≤–≤–µ–ª 'stop'
        print("–í—ã –æ—Ç–º–µ–Ω–∏–ª–∏ –æ–ø–µ—Ä–∞—Ü–∏—é")
        return  # –í—ã—Ö–æ–¥–∏–º –∏–∑ —Ñ—É–Ω–∫—Ü–∏–∏

    # –ó–∞–ø—Ä–∞—à–∏–≤–∞–µ–º –ø—Ä–∏–æ—Ä–∏—Ç–µ—Ç –∑–∞–¥–∞—á–∏ —Å –≤–æ–∑–º–æ–∂–Ω–æ—Å—Ç—å—é –æ—Ç–º–µ–Ω—ã
    priority = proverka_stop("–í—ã–±–µ—Ä–∏—Ç–µ –ø—Ä–∏–æ—Ä–∏—Ç–µ—Ç (–Ω–∏–∑–∫–∏–π/—Å—Ä–µ–¥–Ω–∏–π/–≤—ã—Å–æ–∫–∏–π): ")
    if priority is None:  # –ï—Å–ª–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –≤–≤–µ–ª 'stop'
        print("–í—ã –æ—Ç–º–µ–Ω–∏–ª–∏ –æ–ø–µ—Ä–∞—Ü–∏—é")
        return  # –í—ã—Ö–æ–¥–∏–º –∏–∑ —Ñ—É–Ω–∫—Ü–∏–∏

    # –î–ª—è –Ω–æ–≤—ã—Ö –∑–∞–¥–∞—á –∞–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–∏ —É—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º —Å—Ç–∞—Ç—É—Å "–í –ø—Ä–æ—Ü–µ—Å—Å–µ"
    status = "–í –ø—Ä–æ—Ü–µ—Å—Å–µ"

    # –ó–∞–ø—Ä–∞—à–∏–≤–∞–µ–º –¥–∞—Ç—É —Å–æ–∑–¥–∞–Ω–∏—è —Å –≤–æ–∑–º–æ–∂–Ω–æ—Å—Ç—å—é –æ—Ç–º–µ–Ω—ã
    created_data = proverka_stop("–î–î.–ú–ú.–ì–ì–ì–ì:")
    if created_data is None:  # –ï—Å–ª–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –≤–≤–µ–ª 'stop'
        print("–í—ã –æ—Ç–º–µ–Ω–∏–ª–∏ –æ–ø–µ—Ä–∞—Ü–∏—é")
        return  # –í—ã—Ö–æ–¥–∏–º –∏–∑ —Ñ—É–Ω–∫—Ü–∏–∏

    # –í—Å—Ç–∞–≤–ª—è–µ–º –Ω–æ–≤—É—é –∑–∞–¥–∞—á—É –≤ –±–∞–∑—É –¥–∞–Ω–Ω—ã—Ö
    # –ò—Å–ø–æ–ª—å–∑—É–µ–º –ø–∞—Ä–∞–º–µ—Ç—Ä–∏–∑–æ–≤–∞–Ω–Ω—ã–π –∑–∞–ø—Ä–æ—Å –¥–ª—è –±–µ–∑–æ–ø–∞—Å–Ω–æ—Å—Ç–∏ (–∑–∞—â–∏—Ç–∞ –æ—Ç SQL-–∏–Ω—ä–µ–∫—Ü–∏–π)
    c.execute("INSERT OR IGNORE INTO tasks (task_name , priority, status, created_data ) VALUES (?, ?, ?, ?)",
              (task_name, priority, status, created_data))

    # –°–æ—Ö—Ä–∞–Ω—è–µ–º –∏–∑–º–µ–Ω–µ–Ω–∏—è –≤ –±–∞–∑–µ –¥–∞–Ω–Ω—ã—Ö
    db.commit()
    print("–ó–∞–¥–∞—á–∞ –¥–æ–±–∞–≤–ª–µ–Ω–∞!")


# –§—É–Ω–∫—Ü–∏—è –¥–ª—è –∏–∑–º–µ–Ω–µ–Ω–∏—è —Å—Ç–∞—Ç—É—Å–∞ –∑–∞–¥–∞—á–∏
def complete_task():
    print("\n=== –ò–ó–ú–ï–ù–ò–¢–¨ –°–¢–ê–¢–£–° –ó–ê–î–ê–ß–ò ===")

    # –ó–∞–ø—Ä–∞—à–∏–≤–∞–µ–º ID –∑–∞–¥–∞—á–∏ –¥–ª—è –∏–∑–º–µ–Ω–µ–Ω–∏—è
    id_input = input("–í–≤–µ–¥–∏—Ç–µ ID –∫–æ–Ω–∫—Ä–µ—Ç–Ω–æ–π –∑–∞–¥–∞—á–∏, —á—Ç–æ –±—ã –∏–∑–º–µ–Ω–∏—Ç—å —Å—Ç–∞—Ç—É—Å: ")

    # –ü—Ä–æ–≤–µ—Ä—è–µ–º, —á—Ç–æ –≤–≤–µ–¥–µ–Ω–æ —á–∏—Å–ª–æ (isdigit() –ø—Ä–æ–≤–µ—Ä—è–µ—Ç, —Å–æ—Å—Ç–æ–∏—Ç –ª–∏ —Å—Ç—Ä–æ–∫–∞ —Ç–æ–ª—å–∫–æ –∏–∑ —Ü–∏—Ñ—Ä)
    if not id_input.isdigit():
        print("‚ùå –û—à–∏–±–∫–∞: –Ω—É–∂–Ω–æ –≤–≤–µ—Å—Ç–∏ —á–∏—Å–ª–æ (–Ω–∞–ø—Ä–∏–º–µ—Ä: 1, 2, 3)!")
        return  # –í—ã—Ö–æ–¥–∏–º –∏–∑ —Ñ—É–Ω–∫—Ü–∏–∏

    # –ü—Ä–µ–æ–±—Ä–∞–∑—É–µ–º —Å—Ç—Ä–æ–∫—É –≤ —Ü–µ–ª–æ–µ —á–∏—Å–ª–æ
    task_id = int(id_input)

    # –ò—â–µ–º –∑–∞–¥–∞—á—É —Å —É–∫–∞–∑–∞–Ω–Ω—ã–º ID –≤ –±–∞–∑–µ –¥–∞–Ω–Ω—ã—Ö
    # –ó–∞–ø—Ä–æ—Å: "–ù–∞–π–¥–∏ –í–°–ï (*) –∑–∞–ø–∏—Å–∏ –≤ —Ç–∞–±–ª–∏—Ü–µ tasks –ì–î–ï id —Ä–∞–≤–µ–Ω ?"
    c.execute("SELECT * FROM tasks WHERE id = ?", (task_id,))

